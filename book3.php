    <div class="a"><p>TYPES OF SPEECH</p></div>
    <div class="b"><a>ACT</a></div>
    <div class="border"></div>
    <div class="aa"><a>We all know that speech context and speech styles are just two parts of a communicative strategy. The third prerequisite is the Speech Act. We know that we perform all sorts of ‘speech acts’ besides making statements. These serve as vehicles for all sorts of social activity. By this time, we will be focusing on the types of speech acts.</a></div>
    <div class="container2"></div>
   <div class="brain"><img src="img/7design.png"></div>
   <div class="text1">WHAT IT IS</div>
   <div class="text2"><a>Making a statement may be the primary use of language, but there are other things we can do with words. We can make requests, ask questions, give orders, make promises, give thanks or offer apologies. Moreover, almost any speech act is really the performance of several acts at once, distinguished by different aspects of the speaker’s intention; there is the act of saying something, what one does in saying such, such as requesting or promising, and how one is trying to affect one’s audience. </a></div>
    <div class="text3"><a>Speech acts are speakers’ utterances which convey meaning and make listeners do specific things.</a></div>
    <div class="text33"><a>Speech act is an act of communication. As we communicate with others, we use language without minding whether to use complete sentences or not. The number of words in a single utterance does not matter so long as we can get our message across.</a></div> 
    <div class="text333"><a>According to J. L. Austin (1962), a philosopher and the developer of the Speech Act Theory, there are three types of act in every utterance, given the right circumstances or context. These are:</a></div>
   
    <div class="one"><img src="img/1design.png"></div>
     <div class="txt1">Lucotionary Act</div>
     <div class="text4">this refers to the actual utterance of the speaker. This act happens with the utterance of a sound, a word, or even a phrase as a natural unit of speech.</div>
     <div class="text5">For the utterance to be a Locutionary Act, consider the following:</div>
    </div>
    <br><br>

    <div class="container3">
    <div class="bullet1"></div>
     <div class="text6">It has sense, and mostly important, for communication to take place.</div>
     <div class="bullet2"></div>
     <div class="text7">It has the same meaning to both the speaker and the listener.</div>
     <div class="bullet3"></div>
     <div class="text8">Utterances give rise to shared meaning when it is adjusted by the speaker for the listener.</div>
     <div class="text9"><ee>Examples:</ee></div>
     <div class="bullet4"></div>
     <div class="text10">When you ask someone,<i style="font-weight:bolder"> “Please do the dishes.” </i>– The literal meaning concerns the hearer’s ability to do the dishes</div>
    
     <div class="two"><img src="img/2design.png"></div>
     <div class="txt2">Illucotionary Act</div>
     <div class="text11">Illocutionary act is the social function of what is said.</div>
     <div class="text12"><ee>Examples:</ee></div>
     <div class="bullet5"></div>
     <div class="text13"><b>“Please do the dishes.” </b> – In illocutionary, it is being interpreted by the receiver that the speaker requested him to wash the dishes..</div>

     <div class="three"><img src="img/3design.png"></div>
     <div class="txt3">Perlocutionary Act</div>
     <div class="text14">Perlocutionary act is the resulting act of what is said. This effect is based on the particular context in which the speech act was mentioned</div>
     <div class="text15"><ee>Examples:</ee></div>
     <div class="bullet6"></div>
     <div class="text16"><b>“Please do the dishes”</b> would lead to the addressee washing the dishes.</div>
     <div class="container4"></div>
     <div class="text17">Searle’s Classifications of Speech Act</div>
     <div class="text18"><a>As a response to Austin’s Speech Act Theory, John Searle (1976), a professor from the University of California, Berkeley, classified illocutionary acts into five distinct categories.</a></div>
    </div>
    <br><br>
    <div class="container5">
    <div class="image1"><img src="img/1.png"></div>
    <div class="txt4">Assertive</div>
    <div class="text19">It is an act in which the speaker expresses belief about the truth of a proposition.</div>
    <div class="txt5">Examples:</div>
    <div class="bullet7"></div>
    <div class="text20">boasting.</div>
    <div class="bullet8"></div>
    <div class="text21">concluding.</div>
    <div class="bullet9"></div>
    <div class="text22">putting forward.</div>

    <div class="image2"><img src="img/2.png"></div>
    <div class="txt6">Directive</div>
    <div class="text23">Illocutionary act in which the speaker tries to make the addressee perform an action. </div>
    <div class="txt7">Examples:</div>
    <div class="bullet10"></div>
    <div class="text24">asking</div>
    <div class="bullet11"></div>
    <div class="text25">ordering</div>
    <div class="bullet12"></div>
    <div class="text26">requesting</div>

    <div class="image3"><img src="img/3.png"></div>
    <div class="txt8">Commissive</div>
    <div class="text27">type of illocutionary act which commits the speaker to doing something in the future. </div>
    <div class="txt9">Examples:</div>
    <div class="bullet13"></div>
    <div class="text28">promising</div>
    <div class="bullet14"></div>
    <div class="text29">planning</div>
    <div class="bullet15"></div>
    <div class="text30">vowing</div>
    <div class="bullet16"></div>
    <div class="text31">betting </div>
    
    <div class="image4"><img src="img/4.png"></div>
    <div class="txt10">Expressive</div>
    <div class="text32">a type of illocutionary act in which the speaker expresses his/her feelings or emotional reactions.  </div>
    <div class="txt11">Examples:</div>
    <div class="bullet17"></div>
    <div class="text323">thanking</div>
    <div class="bullet18"></div>
    <div class="text34">apologizing</div>
    <div class="bullet19"></div>
    <div class="text35">welcoming</div>
    </div>
    <br><br>
    <div class="container6">
    <div class="image5"><img src="img/5.png"></div>
    <div class="txt12">Declaration</div>
    <div class="text36">a type of illocutionary act which brings a change in the external situation. Simply put, declarations bring into existence or cause the state of affairs which they refer to. </div>
    <div class="txt13">Examples:</div>
    <div class="bullet20"></div>
    <div class="text37">firing</div>
    <div class="bullet21"></div>
    <div class="text38">baptizing</div>
    <div class="bullet22"></div>
    <div class="text39">bidding</div>
    <div class="bullet23"></div>
    <div class="text40">excommunicating.</div>
