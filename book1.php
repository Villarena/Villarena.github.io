   <div class="text2"><a>Speech Context is important as it helps you communicate
     appropriately, understand the meaning of any message conveyed, and 
     respond accordingly. In order for you to have a clear grasp of what
     speech context is, its types are discussed below.</a></div>
     <div class="text3"><a>Types of Speech Context</a></div>
     <div class="one"><img src="img/1design.png"></div>
     <div class="txt1">Intrapersonal</div> 
     <div class="text4"><a> this refers to communication that centers on one person
       where the speaker acts both as the sender and the receiver of
        message.</a> </div>
    <div class="text5"><a>The message is made up of your thoughts and feelings. The
       channel is your brain, which processes what you are thinking and
        feeling. There is feedback in the sense that as you talk to yourself,
       you discard certain ideas and replace them with others. (Hybels & Weaver, 2012, p 16)</a></div>
       <div class="text55">Murphy (2004) enumerates several examples of intrapersonal communication:</div>
       <div class="text6"><a>1. sense-making like interpreting maps, texts, signs, and symbols;</a>
 <div class="text7"><bb>2. interpreting non-verbal communication like gestures and eye contact</bb>
 <div class="text8"><c>3. communicating between body parts like, “</c>
 <div class="text9"><d>4. speaking aloud (talking to oneself)</d>
 <div class="text10"><e>5. repeating what one hears.</e>
    </div></div>
    <br><br>
    <div class="container3">
      <div class="text11"><ee>Example:</ee></div>
      <div class="bullet"></div>
      <div class="text12"><ff> There is a voice within you that tells you, “It’s okay, you can still do it! You can make it!” when you are losing your drive to finish the task that you are doing.</ff></div>
      <div class="bullet1"></div>
      <div class="text13"><gg>  When you are reflecting or contemplating about something.</gg></div>   
      <div class="bullet2"></div>
      <div class="text14"><hh>  When you told yourself not to talk to your friends when you read in a Facebook post that they were at a party and were not invited.</hh></div>
      <div class="two"><img src="img/2design.png"></div>
      <div class="txt2">Interpersonal</div>
      <div class="text15"><a>This refers to communication between and among people and establishes personal relationship between and among them. </a></div>
      <div class="text16"><aa>Solomon and Theiss (2013) state that “the inter part of the word highlights how interpersonal communication connects people… when you engage in interpersonal communication, you and another person become linked together… The personal part means that your unique qualities as a person matter during interpersonal communication…” (p. 5)</aa></div>
      <div class="border1"><div class="aaa">TYPES OF INTERPERSONAL CONTEXT</div></div>
      <div class="design1"><img src="img/1.png"></div>
      <div class="txt3">Dyad Communication</div>
      <div class="text17"><bb>communication that occurs between two people.</bb></div>
      <div class="txt4">Examples:</div>
      <div class="bullet3"></div>
      <div class="text18">You offered feedback on the speech performance of your classmate.</div>
      <div class="bullet4"></div>
      <div class="text19">You provided comfort to a friend who was feeling down.</div>
      <div class="design2"><img src="img/2.png"></div>
      <div class="txt5">Small Group Communication</div>
      <div class="text20"><bb>This refers to communication that involves at least three but not more than twelve people engaging in a face-to-face interaction to achieve a desired goal.</bb></div>
      <div class="txt6">Examples:</div>
      <div class="bullet5"></div>
      <div class="text21">You are participating in an organizational meeting which aims to address the concerns of your fellow students. </div>
      <div class="bullet6"></div>
      <div class="text22">You are having a discussion with your group mates on how to finish the assigned tasks.</div>
    </div>
    <br><br>
    <div class="container4">
    <div class="design3"><img src="img/3.png"></div>
    <div class="txt7">Public Communication</div>
    <div class="text23"><bb>This type refers to communication that requires you to deliver or send the message before or in front of a group. The message can be driven by informational or persuasive purposes</bb> </div>
    <div class="txt8">Examples:</div>
    <div class="bullet7"></div>
      <div class="text21">You deliver a graduation speech to your batch. </div>
      <div class="bullet8"></div>
      <div class="text22">You participate in a declamation, oratorical, or debate contest watched by a number of people.</div>
      <div class="design4"><img src="img/4.png"></div>
      <div class="txt9">Mass Communication</div>
      <div class="text24"><bb>This refers to communication that takes place through television, radio, newspapers, magazines, books, billboards, internet, and other types of media.</bb> </div>
      <div class="txt10">Examples:</div>
      <div class="bullet9"></div>
      <div class="text25">You are a student journalist articulating your stand on current issues through the school’s newspaper</div>
