      <div class="text27">The environment where the communication happens and how the message is relayed during the process is what we called Speech Context.
</div>
      <div class="bullet11"></div>
      <div class="text28">Speech Context has four types; intrapersonal, interpersonal, public communication, and mass communication.
</div>
