   <div class="text1">WHAT IT IS</div>
   <div class="text2"><a> Just like how we dress up, there is also a scale of formality on how we talk. You may have noticed that the forms of language you use in talking to different people in different situations changes in degree of formality. The context dictates and affects the way people communicate which results in various speech styles. </a></div>
     <div class="text3"><a>According to Joos (1968), speech style refers to a form of language that the speaker utilized which is characterized by the level of formality. He determined five types of speech style: frozen, formal, consultative, casual, and intimate</a></div>
     
     
     <div class="one"><img src="img/1design.png"></div>
     <div class="txt1">Intimate</div>
     <div class="text4"><a>This style is private, which occurs between or among close family members or individuals. The language used in this style may not be shared in public</a> </div>
     <div class="text5"><ee>Examples:</ee></div>
     <div class="bullet1"></div>
     <div class="text6">Couple talking about their future plans</div>
     <div class="bullet2"></div>
     <div class="text7">Family sharing ideas</div>
     <div class="bullet3"></div>
     <div class="text8">Very close friends sharing secrets</div>


     <div class="two"><img src="img/2design.png"></div>
     <div class="txt2">Casual</div>
     <div class="text9">This is used in informal communication between groups and peers who have something to share. In this style, there is a free and easy participation of both speaker and listener. Slang and vernacular language are used in these instances.</div>
     <div class="text10"><ee>Examples:</ee></div>
     <div class="bullet4"></div>
     <div class="text11">Everyday conversation with friends or family</div>
     <div class="bullet5"></div>
     <div class="text12">Phone Calls</div>
     </div>
     <br><br>
     <div class="container3">
     <div class="three"><img src="img/3design.png"></div>
     <div class="txt3">Consultative</div>
     <div class="text13">This style is the standard one. Professional or mutually acceptable language is a must in this style</div>
     <div class="text14"><ee>Examples:</ee></div>
     <div class="bullet6"></div>
     <div class="text15">Classroom discussion</div>
     <div class="bullet7"></div>
     <div class="text16">Parent-teacher conference</div>
     <div class="bullet8"></div>
     <div class="text17">Conversation between an employee and employer</div>

     <div class="four"><img src="img/4design.png"></div>
     <div class="txt4">Formal</div>
     <div class="text18">This type of style is generally used in formal events. It is a one-way straightforward speech that uses formal words and expressions that has been prepared beforehand. </div>
     <div class="text19"><ee>Examples:</ee></div>
     <div class="bullet9"></div>
     <div class="text20">State of the Nation Address</div>
     <div class="bullet10"></div>
     <div class="text21">Sermons by priests and minister</div>
     <div class="bullet11"></div>
     <div class="text22">Business proposal</div>


     <div class="five"><img src="img/5design.png"></div>
     <div class="txt5">Frozen</div>
     <div class="text23">Also known as fixed speech is the highest form of communicative style that is used in a respectful situation or solemn ceremonies.  </div>
